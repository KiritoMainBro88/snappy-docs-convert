    public static string Get(Language language, string key)
    {
        var source = language == Language.Vietnamese ? Vietnamese : English;
        return source.TryGetValue(key, out var value)
            ? value
            : English.TryGetValue(key, out var fallback)
                ? fallback
                : key;
    }
